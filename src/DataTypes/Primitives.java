package DataTypes;

public class Primitives {
    public static void main(String[] args){
        int first = 45;
        long second = 1234;
        byte third = 5;
        short fourth = 2;
        boolean fifth = true;
        char sixth = 'w';
        double seventh = 2.3;
        float eighth = 35F;

        System.out.println("int: " + first);
        System.out.println("long: " + second);
        System.out.println("byte: " + third);
        System.out.println("short: " + fourth);
        System.out.println("boolean " + fifth);
        System.out.println("char: " + sixth);
        System.out.println("double: " + seventh);
        System.out.println("float: " + eighth);
    }
}
